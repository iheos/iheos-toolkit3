package gov.nist.hit.ds.simSupport.exception;

public class SimEngineException extends Exception {

	public SimEngineException() {
		super();
	}

	public SimEngineException(String message, Throwable cause) {
		super(message, cause);
	}

	public SimEngineException(String message) {
		super(message);
	}

	public SimEngineException(Throwable cause) {
		super(cause);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = -2167489156360316603L;

}
